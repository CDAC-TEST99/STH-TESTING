package formFlowX.vo;

import org.simpleframework.xml.Root;

@Root
public class DataServiceVO {
	
	
	private String serviceId;
	private String moduleName;
	private String serviceName;
	private String serviceInternalName;
	private String serviceType;
	private String serviceVersion;
	private String modeOfQuery;
	private String mainFetchQuery;
	private String mainProcedureName;
	private String mainHTML;
	private String isCacheData;
	private String webserviceJsonType;
	
	public String getServiceId() {
		return serviceId;
	}
	public void setServiceId(String serviceId) {
		this.serviceId = serviceId;
	}
	public String getModuleName() {
		return moduleName;
	}
	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}
	public String getServiceName() {
		return serviceName;
	}
	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}
	public String getServiceInternalName() {
		return serviceInternalName;
	}
	public void setServiceInternalName(String serviceInternalName) {
		this.serviceInternalName = serviceInternalName;
	}
	public String getServiceType() {
		return serviceType;
	}
	public void setServiceType(String serviceType) {
		this.serviceType = serviceType;
	}
	public String getServiceVersion() {
		return serviceVersion;
	}
	public void setServiceVersion(String serviceVersion) {
		this.serviceVersion = serviceVersion;
	}
	public String getModeOfQuery() {
		return modeOfQuery;
	}
	public void setModeOfQuery(String modeOfQuery) {
		this.modeOfQuery = modeOfQuery;
	}
	public String getMainFetchQuery() {
		return mainFetchQuery;
	}
	public void setMainFetchQuery(String mainFetchQuery) {
		this.mainFetchQuery = mainFetchQuery;
	}
	public String getMainProcedureName() {
		return mainProcedureName;
	}
	public void setMainProcedureName(String mainProcedureName) {
		this.mainProcedureName = mainProcedureName;
	}
	public String getMainHTML() {
		return mainHTML;
	}
	public void setMainHTML(String mainHTML) {
		this.mainHTML = mainHTML;
	}
	public String getIsCacheData() {
		return isCacheData;
	}
	public void setIsCacheData(String isCacheData) {
		this.isCacheData = isCacheData;
	}
	public String getWebserviceJsonType() {
		return webserviceJsonType;
	}
	public void setWebserviceJsonType(String webserviceJsonType) {
		this.webserviceJsonType = webserviceJsonType;
	}
	
	
	
}
