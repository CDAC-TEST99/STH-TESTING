package thirdpartyservices.bhavishya.model;

import hisglobal.utility.Usefulmethods;

public class BhavishyaRelations {

	private String titleCode;
	private String name;
	private String dob;
	private String genderCode;
	private String relationWithPensioner;
	private String photograph;
	private String handicapped;
	private String handicapped_Certificate;
	private String maritalStatus;
	
	public String getMaritalStatus() {
		return maritalStatus;
	}
	public void setMaritalStatus(String maritalStatus) {
		this.maritalStatus = maritalStatus;
	}
	public String getTitleCode() {
		return titleCode;
	}
	public void setTitleCode(String titleCode) {
		this.titleCode = titleCode;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDob() {
		return Usefulmethods.convertDateFormat("yyyy-MM-dd", "dd-MM-yyyy", dob);
	}
	public void setDob(String dob) {
		this.dob = dob;
	}
	public String getGenderCode() {
		return genderCode;
	}
	public void setGenderCode(String genderCode) {
		this.genderCode = genderCode;
	}
	public String getRelationWithPensioner() {
		return relationWithPensioner;
	}
	public void setRelationWithPensioner(String relationWithPensioner) {
		this.relationWithPensioner = relationWithPensioner;
	}
	public String getPhotograph() {
		return photograph;
	}
	public void setPhotograph(String photograph) {
		this.photograph = photograph;
	}
	public String getHandicapped() {
		return handicapped;
	}
	public void setHandicapped(String handicapped) {
		this.handicapped = handicapped;
	}
	public String getHandicapped_Certificate() {
		return handicapped_Certificate;
	}
	public void setHandicapped_Certificate(String handicapped_Certificate) {
		this.handicapped_Certificate = handicapped_Certificate;
	}
	
}
