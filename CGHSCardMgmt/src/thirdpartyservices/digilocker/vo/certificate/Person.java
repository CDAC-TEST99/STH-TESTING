package thirdpartyservices.digilocker.vo.certificate;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlAttribute;

@XmlAccessorType(XmlAccessType.FIELD)
public  class Person {

    @XmlAttribute
    private String uid;

    @XmlAttribute
    private String title;

    @XmlAttribute
    private String name;

    @XmlAttribute
    private String dob;

    @XmlAttribute
    private String age;

    @XmlAttribute
    private String swd;

    @XmlAttribute
    private String swdIndicator;

    @XmlAttribute
    private String motherName;

    @XmlAttribute
    private String gender;

    @XmlAttribute
    private String maritalStatus;

    @XmlAttribute
    private String relationWithHof;

    @XmlAttribute
    private String disabilityStatus;

    @XmlAttribute
    private String category;

    @XmlAttribute
    private String religion;

    @XmlAttribute
    private String phone;

    @XmlAttribute
    private String email;

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDob() {
		return dob;
	}

	public void setDob(String dob) {
		this.dob = dob;
	}

	public String getAge() {
		return age;
	}

	public void setAge(String age) {
		this.age = age;
	}

	public String getSwd() {
		return swd;
	}

	public void setSwd(String swd) {
		this.swd = swd;
	}

	public String getSwdIndicator() {
		return swdIndicator;
	}

	public void setSwdIndicator(String swdIndicator) {
		this.swdIndicator = swdIndicator;
	}

	public String getMotherName() {
		return motherName;
	}

	public void setMotherName(String motherName) {
		this.motherName = motherName;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getMaritalStatus() {
		return maritalStatus;
	}

	public void setMaritalStatus(String maritalStatus) {
		this.maritalStatus = maritalStatus;
	}

	public String getRelationWithHof() {
		return relationWithHof;
	}

	public void setRelationWithHof(String relationWithHof) {
		this.relationWithHof = relationWithHof;
	}

	public String getDisabilityStatus() {
		return disabilityStatus;
	}

	public void setDisabilityStatus(String disabilityStatus) {
		this.disabilityStatus = disabilityStatus;
	}

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public String getReligion() {
		return religion;
	}

	public void setReligion(String religion) {
		this.religion = religion;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}


}

