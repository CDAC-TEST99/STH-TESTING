package thirdpartyservices.digilocker.vo;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;

import thirdpartyservices.digilocker.vo.PullURIResponse.IssuedTo;

    @XmlAccessorType(XmlAccessType.FIELD)
    public class DocDetailsURI {

        @XmlElement(name = "IssuedTo")
        private IssuedTo issuedTo;

        @XmlElement(name = "URI")
        private String uri;

        @XmlElement(name = "DocContent")
        private String docContent;

        @XmlElement(name = "DataContent")
        private String dataContent;

        // Getters and Setters
        public IssuedTo getIssuedTo() {
            return issuedTo;
        }

        public void setIssuedTo(IssuedTo issuedTo) {
            this.issuedTo = issuedTo;
        }

        public String getUri() {
            return uri;
        }

        public void setUri(String uri) {
            this.uri = uri;
        }

        public String getDocContent() {
            return docContent;
        }

        public void setDocContent(String docContent) {
            this.docContent = docContent;
        }

        public String getDataContent() {
            return dataContent;
        }

        public void setDataContent(String dataContent) {
            this.dataContent = dataContent;
        }
  }

   

