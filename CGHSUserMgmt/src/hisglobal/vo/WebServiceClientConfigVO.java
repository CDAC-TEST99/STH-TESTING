package hisglobal.vo;

import javax.xml.bind.annotation.XmlRootElement;

import org.simpleframework.xml.Element;

@XmlRootElement
public class WebServiceClientConfigVO
{
	
	@Element(required=false)
	private String serviceReferenceNo;
	
	@Element(required=false)
	private String serviceReferenceName;
	
	@Element(required=false)
	private String serviceInitialServerURL;
	
	@Element(required=false)
	private String serviceUserName;
	
	@Element(required=false)
	private String servicePassword;
	
	@Element(required=false)
	private String defaultMethod;
	
	
	public String getServiceReferenceNo() {
		return serviceReferenceNo;
	}
	public void setServiceReferenceNo(String serviceReferenceNo) {
		this.serviceReferenceNo = serviceReferenceNo;
	}
	public String getServiceReferenceName() {
		return serviceReferenceName;
	}
	public void setServiceReferenceName(String serviceReferenceName) {
		this.serviceReferenceName = serviceReferenceName;
	}
	public String getServiceInitialServerURL() {
		return serviceInitialServerURL;
	}
	public void setServiceInitialServerURL(String serviceInitialServerURL) {
		this.serviceInitialServerURL = serviceInitialServerURL;
	}
	public String getServiceUserName() {
		return serviceUserName;
	}
	public void setServiceUserName(String serviceUserName) {
		this.serviceUserName = serviceUserName;
	}
	public String getServicePassword() {
		return servicePassword;
	}
	public void setServicePassword(String servicePassword) {
		this.servicePassword = servicePassword;
	}
	public String getDefaultMethod() {
		return defaultMethod;
	}
	public void setDefaultMethod(String defaultMethod) {
		this.defaultMethod = defaultMethod;
	}
	
	
}
